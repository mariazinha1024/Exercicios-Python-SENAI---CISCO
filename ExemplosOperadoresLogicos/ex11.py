#operadores aritméticos
numero1 = int(input("Digite 1 numero: "))
numero2 = int(input("Digite 2 numero: "))
soma = numero1+numero2
subtracao = numero1-numero2
multiplicacao = numero1*numero2
divisao = numero1/numero2
divisaoapenasinteiro = numero1//numero2
potencia = numero1**numero2
resto = numero1%numero2
print(f"A soma é {soma}")
print(f"A subtracao é {subtracao}")
print(f"A multiplicacao é {multiplicacao}")
print(f"A divisao é {divisao}")
print(f"A divisao apenas inteiro é {divisaoapenasinteiro}")
print(f"A potencia é {potencia}")
print(f"O resto é {resto}")